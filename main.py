def calculate_time_threshold():
    return datetime.now(timezone.utc) - timedelta(hours=24)

def safe_extract_metric(tweet, metric_type):
    try:
        element = tweet.find_element(By.XPATH, f".//div[@data-testid='{metric_type}']//span")
        value_str = element.get_attribute("aria-label").split()[0]
        return int(value_str.replace(',', '')) if value_str else 0
    except:
        return 0

def detect_bias(tweet_text):
    """Detect the most likely cognitive bias based on classifier keywords."""
    tweet_text = tweet_text.lower()
    for keyword, bias in CLASSIFIERS.items():
        if keyword.lower() in tweet_text:
            return bias
    return None

def scrape_tweets(driver, handle, cutoff_time):
    driver.get(f"https://x.com/{handle}")
    time.sleep(3)
    
    if DEBUG_MODE:
        driver.save_screenshot(f"{SCREENSHOT_DIR}/06_{handle}_profile.png")
    
    try:
        WebDriverWait(driver, 20).until(
            EC.presence_of_element_located((By.XPATH, "//article[@data-testid='tweet']"))
        )
        if DEBUG_MODE:
            driver.save_screenshot(f"{SCREENSHOT_DIR}/07_{handle}_tweets_loaded.png")
    except Exception:
        if DEBUG_MODE:
            driver.save_screenshot(f"{SCREENSHOT_DIR}/08_{handle}_tweets_error.png")
        return []
    
    all_tweets = []
    seen_tweet_ids = set()
    last_position = driver.execute_script("return window.pageYOffset;")
    scroll_attempts = 0
    oldest_timestamp = None
    consecutive_old_tweets = 0
    start_time = time.time()
    
    scroll_pattern = [random.randint(300, 800) for _ in range(20)]
    
    while scroll_attempts < MAX_SCROLL_ATTEMPTS:
        scroll_distance = scroll_pattern[min(scroll_attempts, len(scroll_pattern)-1)]
        driver.execute_script(f"window.scrollBy(0, {scroll_distance})")
        time.sleep(SCROLL_PAUSE_TIME + random.uniform(0, 1.0))
        
        new_position = driver.execute_script("return window.pageYOffset;")
        if new_position == last_position:
            scroll_attempts += 1
        else:
            scroll_attempts = 0
            last_position = new_position
        
        tweet_elements = driver.find_elements(By.XPATH, "//article[@data-testid='tweet']")
        
        if DEBUG_MODE and scroll_attempts % 5 == 0:
            driver.save_screenshot(f"{SCREENSHOT_DIR}/09_{handle}_scroll_{scroll_attempts}.png")
        
        current_batch = []
        for tweet in tweet_elements:
            try:
                tweet_id = tweet.get_attribute("aria-labelledby")
                if not tweet_id or tweet_id in seen_tweet_ids:
                    continue
                
                seen_tweet_ids.add(tweet_id)
                
                time_element = tweet.find_element(By.TAG_NAME, "time")
                timestamp_str = time_element.get_attribute("datetime")
                
                if timestamp_str.endswith('Z'):
                    timestamp = datetime.strptime(timestamp_str, "%Y-%m-%dT%H:%M:%S.%fZ").replace(tzinfo=timezone.utc)
                else:
                    timestamp = datetime.fromisoformat(timestamp_str)
                
                if oldest_timestamp is None or timestamp < oldest_timestamp:
                    oldest_timestamp = timestamp
                
                if timestamp < cutoff_time:
                    consecutive_old_tweets += 1
                    continue
                else:
                    consecutive_old_tweets = 0
                
                text_div = tweet.find_element(By.XPATH, ".//div[@data-testid='tweetText']")
                tweet_text = text_div.text
                
                bias = detect_bias(tweet_text)
                
                metrics = {
                    'replies': safe_extract_metric(tweet, "reply"),
                    'retweets': safe_extract_metric(tweet, "retweet"),
                    'likes': safe_extract_metric(tweet, "like")
                }
                
                tweet_data = {
                    'user': f"@{handle}",
                    'text': tweet_text,
                    'bias': bias,
                    'timestamp': timestamp.isoformat(),
                    'id': tweet_id,
                    'metrics': metrics
                }
                
                current_batch.append(tweet_data)
                
            except StaleElementReferenceException:
                continue
            except Exception:
                if DEBUG_MODE:
                    traceback.print_exc()
                continue
        
        all_tweets.extend(current_batch)
        
        if consecutive_old_tweets > 15:
            break
        if oldest_timestamp and oldest_timestamp < cutoff_time:
            break
        if len(all_tweets) > 1000:
            break
        if time.time() - start_time > 300:
            break
    
    print(f"Scraped {len(all_tweets)} tweets from @{handle}")
    return all_tweets

def print_tweets(tweets, handle):
    if not tweets:
        print(f"\nNo recent tweets found for @{handle}")
        return
    
    sorted_tweets = sorted(tweets, key=lambda x: x['timestamp'], reverse=True)
    
    print(f"\nScraped {len(sorted_tweets)} tweets from @{handle} in the last 24 hours")
    print(f"Time range: {sorted_tweets[-1]['timestamp']} to {sorted_tweets[0]['timestamp']}")
    print("="*100)
    
    for i, tweet in enumerate(sorted_tweets[:10], 1):
        print(f"Tweet #{i}")
        print(f"Posted at: {tweet['timestamp']}")
        print(f"Content:\n{tweet['text'][:300]}{'...' if len(tweet['text']) > 300 else ''}")
        print(f"Bias: {tweet['bias'] if tweet['bias'] else 'None'}")
        print(f"Engagement: {tweet['metrics']['replies']} replies | "
              f"{tweet['metrics']['retweets']} retweets | "
              f"{tweet['metrics']['likes']} likes")
        print("-"*100)
    
    if len(sorted_tweets) > 10:
        print(f"\nShowing 10 of {len(sorted_tweets)} tweets. Use DEBUG_MODE for full details.")

def save_tweets_to_json(tweets, filename_prefix="tweets_with_bias"):
    output_dir = "data"
    if not os.path.exists(output_dir):
        os.makedirs(output_dir, exist_ok=True)  # Ensure directory exists
    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%d_%H%M")
    filename = f"{filename_prefix}_{timestamp}.json"
    output_path = os.path.join(output_dir, filename)
    print(f"Saving tweets to: {output_path}")
    
    try:
        with open(output_path, 'w', encoding='utf-8') as f:
            json.dump(tweets, f, indent=2, ensure_ascii=False)
        print(f"Successfully saved {len(tweets)} tweets to {output_path}")
        return output_path
    except Exception as e:
        print(f"Error saving file: {str(e)}")
        traceback.print_exc()
        return None

def main():
    print("Starting Twitter scraper with bias detection...")
    print(f"Environment: {os.name}")
    driver = setup_driver()
    print("Driver initialized successfully")
    
    if not login_twitter(driver, TWITTER_USERNAME, TWITTER_PASSWORD):
        print(f"Login failed. TWITTER_USERNAME: {TWITTER_USERNAME}, TWITTER_PASSWORD set: {TWITTER_PASSWORD is not None}")
        driver.quit()
        return
    print("Login successful")
    
    time_threshold = calculate_time_threshold()
    print(f"Scraping tweets since: {time_threshold.strftime('%Y-%m-%d %H:%M UTC')}")
    
    all_tweets = []
    for handle in CREATOR_HANDLES:
        try:
            print(f"\nStarting scrape for @{handle}...")
            start_time = time.time()
            tweets = scrape_tweets(driver, handle, time_threshold)
            duration = time.time() - start_time
            print(f"Scraped {len(tweets)} tweets from @{handle} in {duration:.1f} seconds")
            print_tweets(tweets, handle)
            all_tweets.extend(tweets)
        except Exception as e:
            print(f"Error scraping @{handle}: {str(e)}")
            if DEBUG_MODE:
                traceback.print_exc()
    
    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%d_%H%M")
    print(f"Generated timestamp: {timestamp}")
    save_tweets_to_json(all_tweets)
    output_path = f"data/tweets_with_bias_{timestamp}.json"
    output_path = save_tweets_to_json(all_tweets)
    if output_path and os.path.exists(output_path):
        print(f"File created: {output_path}, size: {os.path.getsize(output_path)} bytes")
    else:
        print("ERROR: File was not created successfully")
        
    driver.quit()
    print(f"Scraping completed. Total tweets: {len(all_tweets)}")
